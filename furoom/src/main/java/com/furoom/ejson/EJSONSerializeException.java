package com.furoom.ejson;

public class EJSONSerializeException extends EJsonException {

	public EJSONSerializeException() {
		super();
	}

	public EJSONSerializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public EJSONSerializeException(String message) {
		super(message);
	}

	public EJSONSerializeException(Throwable cause) {
		super(cause);
	}

	
	
}
