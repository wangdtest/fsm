package com.furoom.ejson;

public class EJSONDeserializeException extends EJsonException {

	public EJSONDeserializeException() {
		super();
	}

	public EJSONDeserializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public EJSONDeserializeException(String message) {
		super(message);
	}

	public EJSONDeserializeException(Throwable cause) {
		super(cause);
	}

}
